# 사칙 연산자 : +, -, *, /
print("5 + 7 =", 5 + 7)
print("5 - 7 =", 5 - 7)
print("5 * 7 =", 5 * 7)
print("5 / 7 =", 5 / 7)
print()

# 정수 나누기 연산자 : //
print("3 / 2 =", 3 / 2)
print("3 // 2 =", 3 // 2) # 몫
print()

# 나머지 연산자 : %
print("5 % 2 =", 5 % 2) # 나머지
print()

# 제곱 연산자 : **
print("2 ** 1 =", 2 ** 1)
print("2 ** 2 =", 2 ** 2)
print("2 ** 3 =", 2 ** 3)
print("2 ** 4 =", 2 ** 4)