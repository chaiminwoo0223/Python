# 문자 선택 연산자 : []
print("안녕하세요"[0])
print("안녕하세요"[1])
print("안녕하세요"[2])
print("안녕하세요"[3])
print("안녕하세요"[4])
print("안녕하세요"[-1])
print("안녕하세요"[-2])
print("안녕하세요"[-3])
print("안녕하세요"[-4])
print("안녕하세요"[-5])
print()

# 문자열 범위 선택 연산자(슬라이싱) : [:]
print("안녕하세요"[1:5])
print("안녕하세요"[0:2])
print("안녕하세요"[1:3])
print("안녕하세요"[2:4])
print("안녕하세요"[3:5])
print("안녕하세요"[1:])
print("안녕하세요"[:3])
print()

